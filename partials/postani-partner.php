	 	<b>U potrazi ste za stručnim kadrom iz tehničko-tehnoloških oblasti i ekonomije?</b>
	 		<br />
	 	<ul>
	 		<li>JobFAIR ’15 Vam omogućava da kroz direktan kontakt sa zainteresovanim mladim stručnjacima pronađete i odaberete mlade ljude najboljih kvalifikacija koji će doprinijeti daljem napretku i razvoju Vaše kompanije</li>
	 		<li>CV  baza Vam  omogućava  da  na  jednostavan  i  brz  način  pretražujete  unesene CV-jeve studenata i diplomaca i vrlo efikasno dođete do potrebnog profila uposlenika </li>
	 	</ul>
	 		<br />
		<b>Želite promovirati i predstaviti Vašu kompaniju drugima?</b>
			<br />
		<ul>
	 		<li>Mi Vam nudimo i mogućnost organizovanja prezentacije za dane Sajma, gdje će Vaša kompanija imati priliku da se na najbolji način predstavi svojoj ciljnoj grupi i potencijalnim budućim partnerima ili klijentima</li>
	 		<li>JobFAIR je prilika da promovišete svoju kompaniju, ali i proizvode i usluge koje Vaša kompanija može ponuditi studentima i svim posjetiocima Sajma. Prošlogodišnji Sajam je posjetilo preko 1000 studenata i diplomaca tehničko-tehnoloških fakulteta i ekonomije. Pored toga, aktivna promocija u trajanju od mjesec dana uključuje priloge o Sajmu u svim sredstvima javnog informisanja, kao  i  promocije  na  svim  tehničko-tehnološkim  fakultetima i  u  Sarajevu  i  šire.  </li>
	 	</ul>
	 		<br />
	 	<b>Želite dati svoj doprinos u kreiranju kvalitetnijeg stručnog kadra?</b>
	 		<br />
	 	Vi, kao  društveno odgovorna  kompanija,  možete pomoći  studentima  da steknu bolji uvid u vještine i kvalifikacije koje su potrebne za zapošljavanje u njihovoj struci. Na taj način oni su u prilici da se tokom školovanja bolje pripreme za posao i postanu konkurentniji na tržištu rada. 
	 		<br /><br />
		<b>Kako Vaša kompanija može učestvovati na Sajmu?</b> 		
			<br />
		Kompanije svoje učešće na Sajmu poslova mogu realizovati kroz neki od ponuđenih paketa. Paketi obuhvataju štand tokom trajanja Sajma, jednogodišnji pristup CV bazi, prezentaciju kompanije, video promociju na dane Sajma, reklamu u brošuri, reklamu na oficijelnoj web stranici Sajma te ostalim promotivnim materijalima, kao i mnoštvo dodatnih opcija i pogodnosti.
			<br /><br />

		Ukoliko ste zainteresovani za učešće na Sajmu zapošljavanja za studente i diplomce tehničko-tehnoloških fakulteta i ekonomije "JobFAIR ‘15 – Iskoristi svoju šansu!" molimo Vas da popunite <a href="#kontakt">kontakt</a> formu, nakon čega će vas neko od organizatora kontaktirati.	
